// 1
document.getElementById('complete-btn-1').addEventListener('click',
    function(){
        const notify_text = document.getElementById('text-info-1');

        const time = new Date().toLocaleString();
        const rcv_notify = document.createElement('p')
        notify_text.innerText = 'You have completed the fix mobile issue '+time;

        notify_text.classList.add('notify');
        // document.getElementById('complete-btn')
    }
)

document.getElementById("clear").addEventListener("click", function() {
    let notificationText = document.getElementById("text-info-1");
    notificationText.innerText = "";
    notificationText.classList.remove("notify"); 
});


